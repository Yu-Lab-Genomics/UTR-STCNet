class TokenEmbed(nn.Module):
    """ Sequence to Token Embedding
    """
    def __init__(self, embed_dim=128, **kwargs):
        super().__init__()

        self.padding_idx = kwargs["padding_idx"]
        self.num_embeddings = kwargs["token_cls"]
        self.embed_dim = embed_dim
        self.embed_tokens = nn.Embedding(
            self.num_embeddings,
            self.embed_dim,
            padding_idx=self.padding_idx,
        )

        # self.proj = nn.Conv1d(embed_dim, embed_dim, kernel_size=patch_size, stride=stride, padding=patch_size // 2)
        self.norm = nn.LayerNorm(embed_dim)

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv1d):
            # fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out = m.kernel_size[0] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()
        elif isinstance(m, nn.Embedding):
            nn.init.normal_(m.weight, mean=0.0, std=0.02)

    def forward(self, x):
        x = self.embed_tokens(x.long())
        # x = self.proj(x).transpose(1, 2)
        x = self.norm(x)

        return x

